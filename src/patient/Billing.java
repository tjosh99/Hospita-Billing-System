    private void IdFieldActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_IdFieldActionPerformed
        try {

            connection = Connector.ConnectDb();
            String sql = "select * from patient where Patient_ID = ?";
            PreparedStatement st = connection.prepareStatement(sql);
            st.setString(1, (String) IdField.getSelectedItem());
            ResultSet rs = st.executeQuery();
            while (rs.next()) {
                NameField.setText(rs.getString("FName") + " " + rs.getString("LName"));
                BdayField.setText(rs.getString("BMonth") + " " + rs.getString("BDay") + " " + rs.getString("BYear"));
                AgeField.setText(rs.getString("age"));
                AddressField.setText(rs.getString("St_Address") + ", " + rs.getString("C_Address") + ", " + rs.getString("Pr_Address"));
                pTypeField.setText(rs.getString("Patient_Type"));
                rTypeField.setText(rs.getString("Room_Type"));
                aDateField.setText(rs.getString("date"));

            }
        } catch (Exception e) {
        }

    }//GEN-LAST:event_IdFieldActionPerformed

