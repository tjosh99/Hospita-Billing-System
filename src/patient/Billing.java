    private void SaveButtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveButtnActionPerformed

        connection = Connector.ConnectDb();
        try {

            if (connection != null) {
                String id  = ((String) IdField.getSelectedItem());
                String date = DateField.getText();
                String aDate = aDateField.getText();
                String dDate = dDateField.getText();
                String name = NameField.getText();
                String Dname = DoctorName.getText();
                String Birthdate = (BdayField.getText());
                String Rtype = (rTypeField.getText());
                String address = (AddressField.getText());
                int age = Integer.parseInt((String) AgeField.getText());
                String discount = ((String) Discount.getSelectedItem());
                String gender = AgeField.getText();
                String pType = pTypeField.getText();
                int rCharges = Integer.parseInt(bRoomCharges.getText());
                int dFee = Integer.parseInt(bDoctorsFee.getText());
                int mFee = Integer.parseInt(bMiscellanous.getText());
                int tFee = Integer.parseInt(bTestFee.getText());
                int lFee = Integer.parseInt(bLabFee.getText());
                double Total = Double.parseDouble(bTotal.getText());
                double Subtotal = Double.parseDouble(SubTotal.getText());

                String sql = "insert into Billing(Date,Admission_Date,Discharge_Date,Patient_ID,Name,BirthDate,P_RoomType,P_Address,P_Age,Doctor_Name,Discount,Gender,Patient_Type,Room_Charges,Doctors_Fee,Lab_Fee,Miscellanous,Test_Fee,Total,subtotal) values (?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?)";
                try {
                    prp = connection.prepareStatement(sql);
                    prp.setString(1, date);
                    prp.setString(2, aDate);
                    prp.setString(3, dDate);
                    prp.setString(4, id);
                    prp.setString(5, name);
                    prp.setString(6, Birthdate);
                    prp.setString(7, Rtype);
                    prp.setString(8, address);
                    prp.setInt(9, age);
                    prp.setString(10, Dname);
                    prp.setString(11, discount);
                    prp.setString(12, gender);
                    prp.setString(13, pType);
                    prp.setInt(14, rCharges);
                    prp.setInt(15, dFee);
                    prp.setInt(16, lFee);
                    prp.setInt(17, mFee);
                    prp.setInt(18, tFee);
                    prp.setDouble(19, Total);
                    prp.setDouble(20, Subtotal);
                    prp.execute();
                    JOptionPane.showMessageDialog(null, "Data Saved");
                } catch (SQLException e) {
                    JOptionPane.showMessageDialog(null, "This patient has already been invoiced");
                }

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Wrong Input");
            e.printStackTrace();
        }


    }//GEN-LAST:event_SaveButtnActionPerformed

