    public void IDField() {
        try {
            connection = Connector.ConnectDb();
            String sql = "select Patient_ID from patient";
            PreparedStatement id = connection.prepareStatement(sql);
            ResultSet rs = id.executeQuery();
            while (rs.next()) {
                String s = rs.getString("Patient_ID");
                IdField.addItem(s);

            }

        } catch (Exception e) {
        }
    }

