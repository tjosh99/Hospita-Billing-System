    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String discount = ((String) Discount.getSelectedItem());

        try {
            if (discount.equals("20%")) {
                double[] R = new double[7];
                R[0] = Double.parseDouble(bRoomCharges.getText());
                R[1] = Double.parseDouble(bDoctorsFee.getText());
                R[2] = Double.parseDouble(bMiscellanous.getText());
                R[3] = Double.parseDouble(bTestFee.getText());
                R[4] = Double.parseDouble(bLabFee.getText());

                R[5] = ((R[0] + R[1] + R[2] + R[3] + R[4]) * .8);
                R[6] = (R[0] + R[1] + R[2] + R[3] + R[4]);
                String subtotal = String.format("%.2f", R[6]);
                String total = String.format("%.2f", R[5]);
                SubTotal.setText(subtotal);
                bTotal.setText(total);
            } else {
                double[] R = new double[6];
                R[0] = Double.parseDouble(bRoomCharges.getText());
                R[1] = Double.parseDouble(bDoctorsFee.getText());
                R[2] = Double.parseDouble(bMiscellanous.getText());
                R[3] = Double.parseDouble(bTestFee.getText());
                R[4] = Double.parseDouble(bLabFee.getText());

                R[5] = (R[0] + R[1] + R[2] + R[3] + R[4]);

                String sum = String.format("%.2f", R[5]);
                SubTotal.setText(sum);
                bTotal.setText(sum);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Input First");

        }
    }//GEN-LAST:event_jButton2ActionPerformed

