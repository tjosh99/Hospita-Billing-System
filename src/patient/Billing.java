    private void bRoomChargesKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_bRoomChargesKeyReleased
        char c = evt.getKeyChar();
        if (!((Character.isDigit(c)
                || (c == KeyEvent.VK_BACK_SPACE)
                || (c == KeyEvent.VK_DELETE)
                || (c == KeyEvent.VK_ENTER)))) {
            evt.consume();
            JOptionPane.showMessageDialog(null, "Invalid Input");
            bRoomCharges.setText("");
        }
    }//GEN-LAST:event_bRoomChargesKeyReleased

